
// Função declarativa
function funcao(){
    console.log('Sou uma mensagem de uma função declarativa');
}

// Função de expressão
var variavelFuncao = function(){
    console.log('Sou uma mensagem de uma função de expressão');
}

// Arrow function
var ArrowFunction = () => {
    console.log("Sou uma mensagem de uma arrow function");
}

funcao();
variavelFuncao();
ArrowFunction();
